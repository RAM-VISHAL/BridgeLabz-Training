package com.sorting.bubblesort.Icecreamrush;

class Flavor {

    private String name;
    private int salesCount;

    public Flavor(String name, int salesCount) {
        this.name = name;
        this.salesCount = salesCount;
    }

    public String getName() {
        return name;
    }

    public int getSalesCount() {
        return salesCount;
    }
}
